/**
 * Get icon with custom size
 */
export function getIcon(name: keyof typeof Icons, size: number = 24): string {
    const icon = Icons[name];
    if (!icon) return '';
    return icon.replace(/width="24"/g, `width="${size}"`).replace(/height="24"/g, `height="${size}"`);
}

/**
 * Get icon wrapped in a container
 */
export function getIconWithContainer(name: keyof typeof Icons, containerClass: string = 'icon-container icon-bg-primary'): string {
    return `<div class="${containerClass}">${Icons[name]}</div>`;
}

/**
 * Get token logo - Subtle, production-ready style
 */
export function getTokenLogo(symbol: string, size: number = 40, address?: string): string {
    const tokenAddresses: Record<string, string> = {
        'PathUSD': '0x20c0000000000000000000000000000000000000',
        'AlphaUSD': '0x20c0000000000000000000000000000000000001',
        'BetaUSD': '0x20c0000000000000000000000000000000000002',
        'ThetaUSD': '0x20c0000000000000000000000000000000000003'
    };

    const tokenAddress = address || tokenAddresses[symbol] || '';
    const imageUrl = tokenAddress ? `https://tokenlist.tempo.xyz/icon/42431/${tokenAddress}` : '';
    const fallbackIconSize = Math.floor(size * 0.5);

    const fallbackSvg = `<svg xmlns="http://www.w3.org/2000/svg" width="${fallbackIconSize}" height="${fallbackIconSize}" viewBox="0 0 24 24" fill="none" stroke="#71717A" stroke-width="1.5"><circle cx="12" cy="12" r="10"/><path d="M12 6v2"/><path d="M12 16v2"/><path d="M9 10.5C9 9.12 10.34 8 12 8s3 1.12 3 2.5c0 1.38-1.34 2.5-3 2.5s-3 1.12-3 2.5c0 1.38 1.34 2.5 3 2.5"/></svg>`;

    return `
        <div style="width:${size}px;height:${size}px;border-radius:50%;background:rgba(255,255,255,0.04);border:1px solid rgba(255,255,255,0.08);display:flex;align-items:center;justify-content:center;flex-shrink:0;overflow:hidden;">
            ${imageUrl ? `<img src="${imageUrl}" alt="${symbol}" style="width:100%;height:100%;border-radius:50%;object-fit:cover;" onerror="this.style.display='none';this.nextElementSibling.style.display='flex';"/><div style="display:none;width:100%;height:100%;align-items:center;justify-content:center;">${fallbackSvg}</div>` : `<div style="display:flex;align-items:center;justify-content:center;">${fallbackSvg}</div>`}
        </div>
    `;
}

/**
 * Get transaction type icon - Subtle, muted style
 */
export function getTxTypeIcon(type: string, size: number = 36): string {
    const iconMap: Record<string, keyof typeof Icons> = {
        'send': 'txSend',
        'mint': 'txMint',
        'swap': 'txSwap',
        'deploy': 'txDeploy',
        'faucet': 'txFaucet',
        'receive': 'txReceive'
    };

    const iconName = iconMap[type] || 'token';
    const icon = getIcon(iconName, size - 12);

    return `
        <div style="width:${size}px;height:${size}px;background:rgba(255,255,255,0.04);border:1px solid rgba(255,255,255,0.08);border-radius:8px;display:flex;align-items:center;justify-content:center;flex-shrink:0;">
            ${icon.replace('stroke="currentColor"', 'stroke="#A1A1AA"')}
        </div>
    `;
}
